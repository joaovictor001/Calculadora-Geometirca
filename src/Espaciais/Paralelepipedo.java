package Espaciais;

public class Paralelepipedo {
    public  double comprimento, altura, largura;

    Paralelepipedo (){

        this.altura = altura;
        this.largura = largura;
        this.comprimento = comprimento;

    }

    public double calculateVolume(){
        return this.comprimento * this.altura * this.largura;
    }
}
